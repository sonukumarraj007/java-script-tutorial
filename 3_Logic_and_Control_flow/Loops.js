// while loop, generally it looks like this,
// while (condition){
// block of code;
// }
// The while loop loops through a block of code as long as a specified condition is true

let i = 0;

while (i < 10) {
  console.log(i);
  i++;
}

// for loop
// for([initialisation]; [condition];[final-expression]){
// block of code;
// }

for (let i = 1; i < 10; i++) {
  console.log(i);
}
