const superHero = "Batman";

switch (superHero) {
  case "Captain America":
    console.log("Never give up!");
    break;

  case "Iron Man":
    console.log("I am Iron Man!");
    break;

  case "Thor":
    console.log("That is my hammer!");
    break;

  case "Black Widow":
    console.log("One shot, one kill");
    break;

  default:
    console.log("Enter a valid superhero name");
}
