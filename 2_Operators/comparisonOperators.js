// Comparison Operators ==> true / false

const a = 5;
const b = 10;

// Greater than
console.log(a > b);

// Greater than equal to
console.log(a >= b);

// Less than
console.log(a < b);

// Less than equal to
console.log(a <= b);

// Loose equality
// Doesn't compare the DATA TYPES
console.log(a == b);

// Not equal
console.log(a != b);

// Strict Equality
// Compares VALUES and DATA TYPES
// Returns true only if both are the same
console.log(a === b);

// Strict Inequality
console.log(a !== b);



// Logical Operators
// AND && => ALL OPERANDS ARE TRUE => TRUE
console.log(true && true && true);
// OR || => ATLEAST ONE OPERAND IS TRUE => TRUE
console.log(true || false);
// NOT ! => Reverses the Boolean value
console.log(!true);

// Assignment Operator
let num = 34;
num += 2;
console.log(num);
