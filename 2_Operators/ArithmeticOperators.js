const a = 5;
const b = 10;
let result = 0;

// Addition
result = a + b;

// Subtraction
result = a - b;

// Multiplication
result = a * b;

// Division
result = a / b;

// Exponent
result = b ** a;

// Modulo
result = a % b;

// Increment and Decrement
result++;
result--;

console.log(result);
